#!/bin/bash
# 测试布局 - 模拟图片在左侧，文字在右侧的效果

# 清屏并移动光标到 (1,1)
echo -ne "\x1b[2J\x1b[H"

# 模拟图片占据左侧 42% 的宽度
# 假设终端宽度 80，图片宽度约 33 列，文字从第 35 列开始

# 模拟 10 行图片（用 █ 字符填充左侧）
for i in {1..10}; do
  echo -ne "\x1b[${i};1H"
  printf '█%.0s' {1..33}
done

# 在右侧输出文字
text_col=35
lines=(
  "第 84 号  【吉】"
  "赏月不如品团子的月兔"
  "铃瑚"
  "吃团子越多变得越强程度的能力"
  ""
  "──"
  "  鲜花跟团子"
  "  哪能相并论"
  "  月亮和团子"
  "  才能比一比。"
)

row=1
for line in "${lines[@]}"; do
  echo -ne "\x1b[${row};${text_col}H${line}"
  ((row++))
done

# 移动光标到输出下方
echo -ne "\x1b[12;1H\n"
